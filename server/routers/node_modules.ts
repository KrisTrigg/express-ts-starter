import * as path from 'path';
import * as express from 'express';

export var router = express.Router();

//routing for node modules
var app = router

//map some modules to a shorter/cleaner path
app.use('/jquery', express.static(path.join(__dirname, '../../node_modules/jquery/dist')));
app.use('/knockout', express.static(path.join(__dirname, '../../node_modules/knockout/build/output')));
app.use('/knockout', express.static(path.join(__dirname, '../../node_modules/knockout-amd-helpers/build')));
app.use('/bootstrap', express.static(path.join(__dirname, '../../node_modules/bootstrap/dist')));

//generically allow get of modules from the node_modules folder.
app.use('', express.static(path.join(__dirname, '../../node_modules')));


//N.B. the client side requirejs config must also have corresponding path mappings for these modules
//e.g. "jquery": "js/vendor/jquery/jquery.min"